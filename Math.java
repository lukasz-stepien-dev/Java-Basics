import static java.lang.Math.*;
public class Math {
    public static void main(String[] args)
    {
        int x = 9;
        System.out.println(x + " to the second power equal " + pow(x, 2));
        System.out.println("Square root of " + x + " equal " + sqrt(x));
        System.out.println("Logarithm of " + x + " equal " + log(x));
        System.out.println("Pi = " + PI);
        System.out.println("E = " + E);
    }
}
